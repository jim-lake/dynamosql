-- NOW / CURRENT_TIMESTAMP
SELECT NOW() IS NOT NULL AS result;
SELECT CURRENT_TIMESTAMP IS NOT NULL AS result;
SELECT NOW() = CURRENT_TIMESTAMP AS result;

-- CURDATE / CURRENT_DATE
SELECT CURDATE() IS NOT NULL AS result;
SELECT CURRENT_DATE IS NOT NULL AS result;
SELECT CURDATE() = CURRENT_DATE AS result;

-- CURTIME / CURRENT_TIME
SELECT CURTIME() IS NOT NULL AS result;
SELECT CURRENT_TIME IS NOT NULL AS result;
SELECT CURTIME() = CURRENT_TIME AS result;

-- DATE function
SELECT DATE("2024-01-15 10:30:00") AS result;
SELECT DATE("2024-12-31 23:59:59") AS result;
SELECT DATE("2024-01-01") AS result;
SELECT DATE(NULL) AS result;

-- DATE_FORMAT
SELECT DATE_FORMAT("2024-01-15", "%Y-%m-%d") AS result;
SELECT DATE_FORMAT("2024-01-15 10:30:00", "%Y-%m-%d") AS result;
SELECT DATE_FORMAT("2024-01-15 10:30:00", "%Y-%m-%d %H:%i:%s") AS result;
SELECT DATE_FORMAT("2024-01-15", "%Y") AS result;
SELECT DATE_FORMAT("2024-01-15", "%m") AS result;
SELECT DATE_FORMAT("2024-01-15", "%d") AS result;
SELECT DATE_FORMAT("2024-12-31", "%Y-%m-%d") AS result;
SELECT DATE_FORMAT(NULL, "%Y-%m-%d") AS result;

-- DATE_FORMAT with more format specifiers
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%H") AS hour;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%i") AS minute;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%s") AS second;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%S") AS second2;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%T") AS time;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%W") AS weekday;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%w") AS weekday_num;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%X") AS year;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%x") AS year2;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%y") AS year_short;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%a") AS weekday_abbr;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%b") AS month_abbr;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%c") AS month_num;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%D") AS day_suffix;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%e") AS day;
SELECT DATE_FORMAT("2024-01-15 14:30:45", "%h") AS hour_12;
SELECT DATE_FORMAT("2024-01-15 14:30:45", "%I") AS hour_12_2;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%k") AS hour_24;
SELECT DATE_FORMAT("2024-01-15 14:30:45", "%l") AS hour_12_3;
SELECT DATE_FORMAT("2024-01-15 10:30:45", "%M") AS month_name;
SELECT DATE_FORMAT("2024-01-15 14:30:45", "%p") AS am_pm;

-- DATEDIFF
SELECT DATEDIFF("2024-01-15", "2024-01-10") AS result;
SELECT DATEDIFF("2024-01-10", "2024-01-15") AS result;
SELECT DATEDIFF("2024-12-31", "2024-01-01") AS result;
SELECT DATEDIFF("2024-01-01", "2024-12-31") AS result;
SELECT DATEDIFF("2024-01-15", "2024-01-15") AS result;
SELECT DATEDIFF(NULL, "2024-01-15") AS result;
SELECT DATEDIFF("2024-01-15", NULL) AS result;

-- FROM_UNIXTIME
SELECT FROM_UNIXTIME(0) AS result;
SELECT FROM_UNIXTIME(1000000000) AS result;
SELECT FROM_UNIXTIME(1700000000) AS result;
SELECT FROM_UNIXTIME(NULL) AS result;

-- Combining date functions
SELECT DATE(NOW()) IS NOT NULL AS result;
SELECT DATE_FORMAT(CURDATE(), "%Y-%m-%d") IS NOT NULL AS result;
SELECT DATEDIFF(CURDATE(), CURDATE()) AS result;
